long CONTROLCLS::Decode(unsigned char *inpak,unsigned char* outdata,long ENDFA)
{
	int  i;
	int j;
	int k;
	int r;
	int c;
//	int N=4096;
	//int F=18;
//	int THRESHOLD=2;
	unsigned char text_buf[4113];	/* ring buffer of size N */

	unsigned int  flags;
	long y=0;
	long yt=0;
	long yz=0;
	for (i = 0; i < 4096 - 18; i++) text_buf[i] = ' ';

	r = 4096 - 18;  flags = 0;
	for ( ; ; ) {
		if (((flags >>= 1) & 256) == 0) {
			  if (yt++==ENDFA) break;
			  c=inpak[y++];
			  flags = c | 0xff00;
		}
		if (flags & 1)
		    {
			if (yt++==ENDFA) break;
			c=inpak[y++];
			outdata[yz++]=(unsigned char)c;///=w
		    text_buf[r++] = c;  r &= (4096 - 1);
		} else {
			  if (yt++==ENDFA) break;
			  i=inpak[y++];
			if (yt++==ENDFA) break;
			j=inpak[y++];
			i |= ((j & 0xf0) << 4);
		    j = (j & 0x0f) + 2;
			for (k = 0; k <= j; k++)
			    {
				c = text_buf[(i + k) & (4096 - 1)];
				outdata[yz++]=(unsigned char)c;///=w
			    text_buf[r++] = c;  r &= (4096 - 1);
			}
		}
	}
	return y;
}

